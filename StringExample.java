// Java program to demonstrate String creation

public class StringExample {

    public static void main(String[] args) {

        // Creating string using string literal
        String literalString = "GeeksforGeeks";
        System.out.println("Using string literal: " + literalString);

        // Creating string using new keyword
        String objectString = new String("GeeksforGeeks");
        System.out.println("Using new keyword: " + objectString);
    }
}
